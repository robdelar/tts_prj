package package2;

public class Calculator {

	public int addNum(int num1, int num2) {
		return num1+num2;
	}
	
	public int subtract(int num1, int num2) {
		return num1-num2;
	}
	
	public int mult(int num1, int num2) {
		return num1*num2;
	}
	
	public int div(int num1, int num2) {
		return num1/num2;
	}
	
	public int square(int num1) {
		return num1*num1;
	}
}
